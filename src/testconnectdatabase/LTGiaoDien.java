    private void showResult(){
        reset();
        dsds = test.getDAUSACH();
        for(DAUSACH ds : dsds){
            defaultTableModel.addRow(new Object[]{ds.getMASACH(),ds.getTUASACH(),ds.getTACGIA(),ds.getNHASANXUAT()});
        }
    }
