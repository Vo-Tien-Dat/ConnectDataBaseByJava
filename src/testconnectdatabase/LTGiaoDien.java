    // CHUC NANG LUU
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String MASACH = jTextField1.getText();
        String TUASACH = jTextField2.getText();
        String TACGIA = jTextField3.getText();
        String NHAXUATBAN = jTextField4.getText();
        if(MASACH.length() == 0 ){
            JOptionPane.showMessageDialog(rootPane, "vui long nhap du lieu"); 
        }
        else{
            DAUSACH ds = new DAUSACH();
            ds.setMASACH(MASACH);
            ds.setTUASACH(TUASACH);
            ds.setNHASANXUAT(NHAXUATBAN);
            ds.setTACGIA(TACGIA);

            if(test.addDAUSACH(ds) == false)     JOptionPane.showMessageDialog(rootPane, "bi trung ma");
            else System.out.println("khong than cong ");
            
            dsds = test.getDAUSACH();
            showResult();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    
    // CHUC NANG XOA
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int index = jTable4.getSelectedRow();
        if(index == -1){
            JOptionPane.showMessageDialog(rootPane, "Hay chon mot dong de xoa");  
        }
        else{
            DAUSACH ds = dsds.get(index);
            if(test.deleteDAUSACH(ds))  System.out.println("xoa thanh cong");
            else System.out.println("xoa khong thanh cong");
        }
        showResult();
    }//GEN-LAST:event_jButton2ActionPerformed

    
